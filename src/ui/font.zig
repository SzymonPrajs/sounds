pub fn glyph(character: u8) *const Glyph {
    if (character >= '0' and character <= '9') return &digit_glyphs[character - '0'];

    const upper = if (character >= 'a' and character <= 'z')
        character - 'a' + 'A'
    else
        character;

    if (upper >= 'A' and upper <= 'Z') return &letter_glyphs[upper - 'A'];

    return switch (character) {
        ' ' => &space,
        '.' => &dot,
        ',' => &comma,
        '-' => &dash,
        '/' => &slash,
        '=' => &equals,
        ':' => &colon,
        '[' => &left_bracket,
        ']' => &right_bracket,
        '+' => &plus,
        '*' => &star,
        '>' => &greater,
        '<' => &less,
        '%' => &percent,
        '_' => &underscore,
        '\'' => &apostrophe,
        '(' => &left_paren,
        ')' => &right_paren,
        '?' => &question,
        else => &space,
    };
}

pub fn textWidth(text: []const u8, scale: i32) i32 {
    if (text.len == 0 or scale <= 0) return 0;
    const glyphs: i32 = @intCast(codepointCount(text));
    return (glyphs * glyph_advance - 1) * scale;
}

pub fn textHeight(scale: i32) i32 {
    return glyph_height * @max(1, scale);
}

pub fn prefixWidth(text: []const u8, bytes: usize, scale: i32) i32 {
    return textWidth(text[0..@min(bytes, text.len)], scale);
}

pub fn codepointCount(text: []const u8) usize {
    var count: usize = 0;
    var offset: usize = 0;
    while (offset < text.len) : (count += 1) {
        offset += utf8Step(text, offset);
    }
    return count;
}

pub fn utf8Step(text: []const u8, offset: usize) usize {
    if (offset >= text.len) return 0;
    const lead = text[offset];
    const size: usize = if ((lead & 0x80) == 0)
        1
    else if ((lead & 0xE0) == 0xC0)
        2
    else if ((lead & 0xF0) == 0xE0)
        3
    else if ((lead & 0xF8) == 0xF0)
        4
    else
        1;

    if (offset + size > text.len) return 1;
    return size;
}

test "font measures ASCII and UTF-8 by visible codepoints" {
    try std.testing.expectEqual(@as(i32, 0), textWidth("", 2));
    try std.testing.expectEqual(@as(i32, 5), textWidth("A", 1));
    try std.testing.expectEqual(@as(i32, 23), textWidth("LIVE", 1));
    try std.testing.expectEqual(@as(i32, 34), textWidth("éab", 2));
    try std.testing.expectEqual(@as(usize, 3), codepointCount("éab"));
}
